# ─── Helpers ───

def parse_upazila(raw):
    if not raw:
        return ''
    raw = raw.strip()
    return raw.replace(' Upazila', '').replace(' upazila', '').strip()


def parse_district(raw):
    if not raw:
        return ''
    raw = raw.strip().replace(' District', '').replace(' district', '').strip()
    return f'{raw} District' if raw else ''


def lookup_division(district_key):
    info = DISTRICTS.get(district_key)
    if info:
        return info['division']
    bare = district_key.replace(' District', '').strip()
    div_key = DIVISION_LOOKUP.get(bare)
    if div_key:
        return div_key
    for dk, dv in DISTRICTS.items():
        if dk.startswith(bare):
            return dv['division']
    return ''


def generate_slug(upazila, district):
    upazila_clean = upazila.replace(',', '').replace('.', '').replace("'", '').replace(' ', '-').strip()
    district_clean = district.replace(',', '').replace('.', '').replace("'", '').replace(' ', '-').strip()
    return f'{upazila_clean}-Upazila-{district_clean}'


def generate_description(upazila, district):
    upazila_bn = ''
    district_bn = ''
    up_info = UPAZILAS.get(upazila)
    if up_info:
        upazila_bn = up_info.get('bn', '') or ''
    dist_info = DISTRICTS.get(district)
    if dist_info:
        raw = dist_info.get('bn', '') or ''
        district_bn = raw.replace(' জেলা', '').strip()
    up_name = upazila_bn or upazila
    dist_name = district_bn or district.replace(' District', '').strip()
    return f'{up_name} উপজেলা, {dist_name} জেলা'


